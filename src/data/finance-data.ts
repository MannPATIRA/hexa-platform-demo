// ─── Demo emails for the AP detail page ───────────────────────────────────────

export function buildAPClarificationDraft(invoice: APInvoice, supplier: APSupplier | undefined): EmailRecord {
  const supplierEmail = supplier?.email ?? "billing@supplier.com";
  return {
    id: `draft-${invoice.id}`,
    direction: "outgoing",
    from: "ap@hexamfg.com",
    to: supplierEmail,
    sentAt: new Date().toISOString(),
    subject: `Discrepancy on ${invoice.invoiceNumber} / ${invoice.poNumber}`,
    body: `Hi ${invoice.supplierName} team,\n\nWe've received ${invoice.invoiceNumber} against ${invoice.poNumber} and our 3-way match flagged the following discrepancy:\n\n${invoice.discrepancySummary}\n\nFor reference:\n- PO total: ${formatMoney(invoice.poTotalAmount, invoice.currency)}\n- Invoice total: ${formatMoney(invoice.totalAmount, invoice.currency)}\n- Discrepancy: ${formatMoney(invoice.discrepancyAmount, invoice.currency)}\n\nCould you confirm whether this was an intentional change or a billing error? We'll hold the invoice from approval pending your reply.\n\nThanks,\nHexa Accounts Payable`,
  };
}

export function formatMoney(amount: number, currency: "GBP" | "USD" | "EUR" = "USD"): string {
  const symbol = currency === "GBP" ? "£" : currency === "EUR" ? "€" : "$";
  return `${symbol}${amount.toLocaleString(undefined, { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
}
