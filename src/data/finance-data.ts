function ts(day: number, hour = 9, minute = 0): string {
  // Anchored timeline so the demo feels coherent. Treat as "today = March 9, 2026".
  const date = new Date(Date.UTC(2026, 2, day, hour, minute, 0));
  return date.toISOString();
}

function buildAPTimeline(events: Array<Omit<AgentEvent, "id">>): AgentEvent[] {
  return events.map((e, i) => ({ ...e, id: `evt-${i + 1}` }));
}

